# Loop through a collection
for name in ['Christopher', 'Susan']:
    print(name)

# Looping a number of times
for index in range(0,3):
    print(index)

# Looping with a condition
names = ['Christopher', 'Susan']
index = 0
while index < len(names):
    print(names[index])
    # Change the condition!!
    index = index + 1
